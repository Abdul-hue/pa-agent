interface CountrySelectProps {
  value: string;
  onChange: (value: string) => void;
  placeholder?: string;
  required?: boolean;
  disabled?: boolean;
}

export function CountrySelect({
  value,
  onChange,
  placeholder = 'Select country',
  required = false,
  disabled = false,
}: CountrySelectProps) {
  const [open, setOpen] = useState(false);

  const selectedCountry = useMemo(
    () => COUNTRIES.find((country) => country.name === value),
    [value]
  );

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className={cn(
            'w-full justify-between bg-white/5 border-white/10 text-white hover:bg-white/10 focus:border-primary focus:ring-primary/50 transition-all duration-300',
            !value && 'text-gray-400'
          )}
          disabled={disabled}
        >
          {selectedCountry ? (
            <span className="flex items-center gap-2">
              <span>{selectedCountry.flag}</span>
              <span>{selectedCountry.name}</span>
            </span>
          ) : (
            placeholder
          )}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-full p-0 bg-gray-900 border-white/10" align="start">
        <Command className="bg-gray-900">
          <CommandInput
            placeholder="Search country..."
            className="text-white placeholder:text-gray-400 border-white/10"
          />
          <CommandEmpty className="text-gray-400 py-6 text-center text-sm">
            No country found.
          </CommandEmpty>
          <CommandGroup className="max-h-64 overflow-auto">
            {COUNTRIES.map((country) => (
              <CommandItem
                key={country.code}
                value={country.name}
                onSelect={(currentValue) => {
                  onChange(currentValue === value ? '' : currentValue);
                  setOpen(false);
                }}
                className="text-white hover:bg-white/10 cursor-pointer"
              >
                <Check
                  className={cn(
                    'mr-2 h-4 w-4',
                    value === country.name ? 'opacity-100' : 'opacity-0'
                  )}
                />
                <span className="mr-2">{country.flag}</span>
                <span>{country.name}</span>
              </CommandItem>
            ))}
          </CommandGroup>
        </Command>
      </PopoverContent>
    </Popover>
  );
}

